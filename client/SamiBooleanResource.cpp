SamiBooleanResource::SamiBooleanResource() {
    init();
}

SamiBooleanResource::~SamiBooleanResource() {
    this->cleanup();
}

void
SamiBooleanResource::init() {
    pValue = null;
}

void
SamiBooleanResource::cleanup() {
    if(pValue != null) {
        
        delete pValue;
        pValue = null;
    }
}


SamiBooleanResource*
SamiBooleanResource::fromJson(String* json) {
    this->cleanup();
    String str(json->GetPointer());
    int length = str.GetLength();

    ByteBuffer buffer;
    buffer.Construct(length);

    for (int i = 0; i < length; ++i) {
       byte b = str[i];
       buffer.SetByte(b);
    }

    IJsonValue* pJson = JsonParser::ParseN(buffer);
    fromJsonObject(pJson);
    if (pJson->GetType() == JSON_TYPE_OBJECT) {
       JsonObject* pObject = static_cast< JsonObject* >(pJson);
       pObject->RemoveAll(true);
    }
    else if (pJson->GetType() == JSON_TYPE_ARRAY) {
       JsonArray* pArray = static_cast< JsonArray* >(pJson);
       pArray->RemoveAll(true);
    }
    delete pJson;
    return this;
}


void
SamiBooleanResource::fromJsonObject(IJsonValue* pJson) {
    JsonObject* pJsonObject = static_cast< JsonObject* >(pJson);

    if(pJsonObject != null) {
        JsonString* pValueKey = new JsonString(L"value");
        IJsonValue* pValueVal = null;
        pJsonObject->GetValue(pValueKey, pValueVal);
        if(pValueVal != null) {
            
            pValue = new Boolean(false);
            jsonToValue(pValue, pValueVal, L"Boolean", L"Boolean");
        }
        delete pValueKey;
    }
}

